CREATE OR REPLACE VIEW SITUACAO AS
SELECT 1 AS "IDSITUACAOERP" ,
 T1."BPLId" AS "IDEMPRESAERP", 
CASE
WHEN T0."validFor" = 'Y' THEN 'Ativo'
ELSE 'Inativo' END AS  "DESCRICAO",
 '' AS "COR",
 CASE
WHEN T0."validFor" = 'Y' THEN 1
ELSE 0 END AS "SITUACAO" ,
 6 AS "LIBERAPEDIDO" FROM OCRD T0 INNER JOIN CRD8 T1 ON T0."CardCode" = T1."CardCode"